package Operators;

public class AssigmentOp {
    public static void main(String[] args){

        int sal = 1000;
        int over = 500;
        sal += over;   //sal = sal+over;
        System.out.println(sal);

        sal *= over;
        System.out.println(sal);

        sal /= over;
        System.out.println(sal);




    }


}
