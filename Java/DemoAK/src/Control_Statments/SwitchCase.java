package Control_Statments;

import java.util.Scanner;


public class SwitchCase {

    public static void main(String[] args) {
        int state = 15;
        switch (state) {

            case 1:
                System.out.println("Gujarat");
                break;
            case 2:
                System.out.println("Rajasthan");
                break;
            case 3:
                System.out.println("Delhi");
                break;
            case 4:
                System.out.println("Maharashtra");
                break;
            case 5:
                System.out.println("Kashmir");
                break;
            case 6:
                System.out.println("Kerla");
                break;
            case 7:
                System.out.println("Panjab");
                break;
            case 8:
                System.out.println("Ladak");
                break;
            case 9:
                System.out.println("Haryana");
                break;
            case 10:
                System.out.println("Uttara Khand");
                break;
            default:
                System.out.println("Not Valid");


        }

    }
}