package Star_Pattern.src;

public class SwapValue {
    public static void main(String[]args)
    {
        int a=100;
        int b=200;
        System.out.println(a);
        System.out.println(b);
        int T ;
        T=a;
        a=b;
        b=T;
        System.out.println(a);
        System.out.println(b);
    }
}
